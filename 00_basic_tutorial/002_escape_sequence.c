/* Escape Sequence in C */


#include <stdio.h>


int main(void) {
  // Horizontal Tab
  printf("Tutorial\tGateway \n");

  // New line
  printf("Welcome To \nTutorial Gateway \n");

  // Single Quote, and Question Mark
  printf("Learn \'C\' Programing\? \v Visit Tutorial Gateway \n");

  // Vertical Tab
  printf("\v");

  // Double Quotes
  printf("\"Tutorial Gateway\" ");

  // Carriage return
  printf("\r");

  return 0;
}
