/* The labs() function returns the absolute value of the specified long
   integral value. */


#include <stdio.h>
#include <stdlib.h>


void main() {
  printf("labs(10) = %ld\n", labs(10));
  printf("labs(-10) = %ld\n", labs(-10));
  printf("labs(50) = %ld\n", labs(50));
  printf("labs(-50) = %ld\n", labs(-50));
}
